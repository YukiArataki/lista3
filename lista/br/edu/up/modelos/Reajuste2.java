package lista.br.edu.up.modelos;

public class Reajuste2 {
    private String nome;
    private double salarioAntigo;
    private double salarioNovo;
    private double porcentagemReajuste;
    private double reajuste;
    private double qtdSalarios;
    
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public double getSalarioAntigo() {
        return salarioAntigo;
    }
    public void setSalarioAntigo(double salarioAntigo) {
        this.salarioAntigo = salarioAntigo;
    }
    public double getSalarioNovo() {
        return salarioNovo;
    }
    public void setSalarioNovo(double salarioNovo) {
        this.salarioNovo = salarioNovo;
    }
    public double getPorcentagemReajuste() {
        return porcentagemReajuste;
    }
    public void setPorcentagemReajuste(double porcentagemReajuste) {
        this.porcentagemReajuste = porcentagemReajuste;
    }
    public double getReajuste() {
        return reajuste;
    }
    public void setReajuste(double reajuste) {
        this.reajuste = reajuste;
    }
    public double getQtdSalarios() {
        return qtdSalarios;
    }
    public void setQtdSalarios(double qtdSalarios) {
        this.qtdSalarios = qtdSalarios;
    }
}